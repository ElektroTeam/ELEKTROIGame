package enums;

/**
 * The states of a battle.
 */
public enum StateOfBattle {
    /**
     * The player won.
     */
    Win,
    /**
     * The player lost.
     */
    Lose,
    /**
     * The player escaped the battle.
     */
    Ran,
    /**
     * The player is ready to fight.
     */
    readyAndGo,
}